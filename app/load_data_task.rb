class LoadDataTask
  attr_reader :status_message, :chunks

  Patch = Struct.new(:chunk_id, :address, :old_value, :new_value)

  MAGIC_STRING = "Created on an Apple ][ GS. Apple ][ Forever!"

  def initialize
    @status_message = "Initializing..."
    @chunks = []
    @state = :start
    @chunk_id = 0
    @chunk_count = 0
    @table = nil
    @patches = get_patches

    @data1_path = "data/DATA1"
    @data2_path = "data/DATA2"
    @exec_path = "data/DRAGON.COM"
  end

  def tick(args)
    case @state
    when :start
      if !$gtk.read_file(@data1_path) || !$gtk.read_file(@data2_path) || !$gtk.read_file(@exec_path)
        @status_message = "Error: Please place DATA1, DATA2, and DRAGON.COM in the 'data' folder."
        @state = :error
        return
      end

      @status_message = "Reading Chunk Table..."
      @table = ChunkTable.new(@data1_path, @data2_path)
      @chunk_count = @table.get_chunk_count
      @state = :loading_chunks

    when :loading_chunks
      if @chunk_id < @chunk_count
        @status_message = "Loading segment #{@chunk_id + 1} of #{@chunk_count + 1}"

        new_chunk = @table.get_modifiable_chunk(@chunk_id)

        if new_chunk.size != 0
          if @chunk_id >= 0x18
            @status_message = "Decompressing segment #{@chunk_id + 1} of #{@chunk_count + 1}"
            huffman = HuffmanDecoder.new(new_chunk)
            new_chunk = ModifiableChunk.new(huffman.decode)

            if @chunk_id <= 0x1d
              @status_message = "Decoding segment #{@chunk_id + 1} of #{@chunk_count + 1}"
              apply_rolling_xor(new_chunk)
            end

            if @chunk_id >= 0x100
              @status_message = "Decoding segment #{@chunk_id + 1} of #{@chunk_count + 1}"
              apply_rolling_addition(new_chunk, 0x0000) if @chunk_id % 2 != 0
              apply_rolling_addition(new_chunk, 0x0004)
            end
          end
        end

        apply_patches_to(new_chunk, @chunk_id)

        @chunks << new_chunk
        @chunk_id += 1
      else
        @state = :loading_exec
      end

    when :loading_exec
      @status_message = "Loading code segment #{@chunk_count + 1} of #{@chunk_count + 1}"

      exec_contents = $gtk.read_file(@exec_path)
      code_segment = exec_contents.bytes

      check_string = MAGIC_STRING.bytes
      check_string.each_with_index do |b, i|
        if code_segment[0x08 + i] != b
          @status_message = "Error: DRAGON.COM does not appear to be valid"
          @state = :error
          return
        end
      end

      code_chunk = Chunk.new(code_segment)
      @chunks << code_chunk

      args.state.chunks = @chunks

      @status_message = "Finished."
      @state = :done

    when :done
      # Loading is complete, do nothing

    when :error
      # In error state, do nothing
    end
  end

  private

  def apply_rolling_addition(chunk, base_index)
    pointer = base_index
    running = 0
    while pointer < chunk.size
      running = running + chunk.get_unsigned_byte(pointer)
      chunk.write(pointer, 1, running)
      pointer += 1
    end
  end

  def apply_rolling_xor(chunk)
    read_address = 0x00
    write_address = 0xa0
    while write_address < chunk.size
      b0 = chunk.get_quad_word(read_address)
      b1 = chunk.get_quad_word(write_address)
      chunk.write(write_address, 4, b0 ^ b1)
      read_address += 4
      write_address += 4
    end
  end

  def apply_patches_to(chunk, id)
    @patches.each do |p|
      if p.chunk_id == id
        old_value = chunk.get_bytes(p.address, p.old_value.size)
        if p.old_value == old_value
          if p.new_value.size != p.old_value.size
            puts "Patch failed to apply: new value is wrong length"
            puts p.inspect
            next
          end
          chunk.set_bytes(p.address, p.new_value)
        else
          puts "Patch failed to apply: old value doesn't match existing"
          puts p.inspect
        end
      end
    end
  end

